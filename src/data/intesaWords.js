export function getRandomWords(categories, difficulty, count) {
  const words = []
  const availableCategories = categories.filter(cat => INTESA_WORDS[cat])
  
  if (availableCategories.length === 0) {
    availableCategories.push('Generale')
  }
  
  for (let i = 0; i < count; i++) {
    const category = availableCategories[i % availableCategories.length]
    const categoryWords = INTESA_WORDS[category][difficulty]
    const randomIndex = Math.floor(Math.random() * categoryWords.length)
    const word = categoryWords[randomIndex]
    
    words.push({
      word,
      category,
      difficulty
    })
  }
  
  // Shuffle words
  return words.sort(() => Math.random() - 0.5)
}

/**
 * Get random words with AI support
 * @param {Array} categories - Selected categories
 * @param {string} difficulty - Difficulty level
 * @param {number} count - Number of words
 * @param {Object} openaiService - OpenAI service instance (optional)
 * @returns {Promise<Array>} Array of word objects
 */
export async function getRandomWordsWithAI(categories, difficulty, count, openaiService = null, usedWords = []) {
  // Try AI first if service is available and configured
  if (openaiService && openaiService.isConfigured()) {
    try {
      const availableCategories = categories.filter(cat => INTESA_WORDS[cat])
      if (availableCategories.length === 0) {
        availableCategories.push('Generale')
      }
      
      const wordsPerCategory = Math.ceil(count / availableCategories.length)
      const allAIWords = []
      
      for (const category of availableCategories) {
        const aiWords = await openaiService.generatePartyWords(category, difficulty, wordsPerCategory, 'intesa')
        
        if (aiWords && Array.isArray(aiWords)) {
          aiWords.forEach(word => {
            // Only add if not already used
            if (!usedWords.includes(word)) {
              allAIWords.push({
                word,
                category,
                difficulty
              })
            }
          })
        }
      }
      
      if (allAIWords.length >= count * 0.7) { // If we got at least 70% of words from AI
        // Shuffle and return
        return allAIWords.slice(0, count).sort(() => Math.random() - 0.5)
      }
    } catch (error) {
      console.warn('Generazione parole fallita, uso fallback:', error)
    }
  }
  
  // Fallback to static words (also filter by used words)
  const staticWords = getRandomWords(categories, difficulty, count * 2) // Get extra to filter
  const filtered = staticWords.filter(w => !usedWords.includes(w.word))
  return filtered.length >= count ? filtered.slice(0, count) : staticWords.slice(0, count)
}

